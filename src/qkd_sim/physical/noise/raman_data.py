def get_raman_gain(
    delta_f: float | np.ndarray,
    f_pump: float | np.ndarray,
    A_eff: float,
) -> float | np.ndarray:
    """根据泵浦-信号频移插值并修正拉曼增益系数 g_R。

    公式 3.1.5 (formulas_sprs.md):
        g_R(f₁, f₂) = g_eff(|Δf|) / A_eff(f_pump)

    g0 在参考条件（reference_frequency、reference_effective_area）下测定。
    修正公式：
        g_R = g0(|Δf|) × (f_pump / f_ref) × (A_eff_ref / A_eff)

    频率修正：拉曼增益效率正比于泵浦频率（参见 GNPY 文档）。
    面积修正：g0 定义在参考面积下，需换算到实际有效模场面积。

    超出插值范围（|Δf| > 42 THz）时返回 0。

    Parameters
    ----------
    delta_f : float or ndarray
        泵浦-信号频移的绝对值 |f₁ - f₂| [Hz]，形状任意
    f_pump : float or ndarray
        泵浦光频率 [Hz]，与 delta_f 广播兼容
    A_eff : float
        光纤有效模场面积 [m²]

    Returns
    -------
    float or ndarray
        拉曼增益系数 g_R [1/(W·m)]，形状与 delta_f 相同

    Examples
    --------
    >>> g_R = get_raman_gain(13e12, 193.5e12, 80e-12)
    >>> float(g_R) > 0
    True
    """
    delta_f = np.asarray(delta_f, dtype=np.float64)
    f_pump = np.asarray(f_pump, dtype=np.float64)

    # 线性插值 g0(|Δf|)，超出范围填 0
    g0_interp = np.interp(delta_f, FREQUENCY_OFFSET, G0, left=0.0, right=0.0)

    # 频率 & 面积修正
    g_R = g0_interp * (f_pump / REFERENCE_FREQUENCY) * (REFERENCE_EFFECTIVE_AREA / A_eff)
    return g_R